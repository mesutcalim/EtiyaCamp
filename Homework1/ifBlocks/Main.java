package Homework1.ifBlocks;

public class Main {
    public static void main(String[] args) {

        int number = 21;

        if (number < 20) {
            System.out.println("The number is less than 20.");
        }
        else if (number == 20) {
            System.out.println("The number is equal to 20.");
        }
        else {
            System.out.println("The number is greater than 20.");
        }
    }
}
